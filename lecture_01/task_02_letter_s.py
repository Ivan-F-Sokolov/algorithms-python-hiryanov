"""
Draw a letter "s".
"""

import turtle

# Make a cursor look like a turtle.
turtle.shape('turtle')

turtle.forward(50)
turtle.left(90)

turtle.forward(50)
turtle.left(90)

turtle.forward(50)
turtle.right(90)

turtle.forward(50)
turtle.right(90)

turtle.forward(50)

# Prevent window from closing so you can see the result.
turtle.mainloop()
