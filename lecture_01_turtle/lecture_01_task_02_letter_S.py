"""
Draws the letter S.
"""

import turtle

# Makes the cursor looks like turtle.
turtle.shape('turtle')

# Moves drawing the letter S.
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.right(90)
turtle.forward(50)
turtle.right(90)
turtle.forward(50)
turtle.penup()

# Stops the turtle so you can see the result.
turtle.mainloop()